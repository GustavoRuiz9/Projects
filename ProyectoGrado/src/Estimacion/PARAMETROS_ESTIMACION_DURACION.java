    public void verificarDatos() {


        tablaModelo = (TableModel) jTable1.getModel();

        boolean avanzar = true;

        int fila, columna;
        fila = jTable1.getSelectedRow();
        columna = jTable1.getSelectedColumn();

        if (fila == -1) {
            avanzar = false;
        } else if (columna == -1) {
            avanzar = false;
        }

        if (avanzar) {
            strResultado = Double.parseDouble(tablaModelo.getValueAt(jTable1.getSelectedRow(), jTable1.getSelectedColumn()).toString());
            JOptionPane.showMessageDialog(null, "Dato seleccionado : " + strResultado);
            System.out.println("Dato 1 " + strResultado);
            Tabla3.tablaModelo2.setValueAt(strResultado, 0, 0);


        } else {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado un registro");
        }



    }

