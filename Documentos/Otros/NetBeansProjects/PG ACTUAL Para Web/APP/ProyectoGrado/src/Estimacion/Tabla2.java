    public void calcular() {

        if (jTable1.getValueAt(0, 0) == null || jTable1.getValueAt(0, 1) == null) {

            JOptionPane.showMessageDialog(null, "Ingrese Todas Las Caracteristicas Antes de Calcular",
                    "Caracteristicas", JOptionPane.ERROR_MESSAGE);

        } else {

            c1 = Double.parseDouble(jTable1.getValueAt(0, 0) + "");
            c2 = Double.parseDouble(jTable1.getValueAt(0, 1) + "");
            pf1 = Double.parseDouble((Math.pow(Tabla1.porcemenos, c2)) + "");
            pf1 = pf1 * c1;
            jTable2.setValueAt(formateador.format(pf1), 0, 1);

            c3 = Double.parseDouble(jTable1.getValueAt(0, 0) + "");
            c4 = Double.parseDouble(jTable1.getValueAt(0, 1) + "");
            pf = Double.parseDouble((Math.pow(Tabla1.pf, c4)) + "");
            pf = pf * c3;
            jTable2.setValueAt(formateador.format(pf), 1, 1);

            c5 = Double.parseDouble(jTable1.getValueAt(0, 0) + "");
            c6 = Double.parseDouble(jTable1.getValueAt(0, 1) + "");
            pf2 = Double.parseDouble((Math.pow(Tabla1.porcemas, c6)) + "");
            pf2 = pf2 * c5;
            jTable2.setValueAt(formateador.format(pf2), 2, 1);
        }

    }

