    public void calcular() {

        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese Valor de Hora", "Valor", JOptionPane.ERROR_MESSAGE);
        } else {

            v1 = new BigDecimal(Tabla2.pf1 * (Double.parseDouble(jTextField1.getText())));
            jTable1.setValueAt(formato.format(v1), 0, 1);
            v2 = new BigDecimal(Tabla2.pf * (Double.parseDouble(jTextField1.getText())));
            jTable1.setValueAt(formato.format(v2), 1, 1);
            v3 = new BigDecimal(Tabla2.pf2 * (Double.parseDouble(jTextField1.getText())));
            jTable1.setValueAt(formato.format(v3), 2, 1);
        }
    }

